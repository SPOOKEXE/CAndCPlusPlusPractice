#include <iostream> // include standard library file

int main() {
	// std means "standard library"
	// cout means "character out"

	// std::cout is an object that represents the "Standard Output Stream"
	// Stream = sequence of characters
	// Standard Output = output / console
	// meaning we can write a sequence of characters to the output.

	// the "<<" is the "Stream Insertion Operator"
	// inserts something into the output stream

	std::cout << "Epic!"; // statement line, terminate with semi-colon

	return 0; // 0 says program terminates correctly
}
